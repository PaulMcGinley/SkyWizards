#include "GameManager.h"

/* ===========================================================================================================================================================================================
 * ===   RESOLUTION   ========================================================================================================================================================================
 * ===========================================================================================================================================================================================
*/

// ********** Getters **********

bool GameManager::isCustomResolution() const {
    return is_custom_resolution;
}

float GameManager::getResolutionRatio() const {
    return static_cast<float>(resolution.x) / static_cast<float>(resolution.y);
}

sf::Vector2<unsigned int> GameManager::getResolution() const {
    return resolution;
}

unsigned int GameManager::getResolutionWidth() const {
    return resolution.x;
}

unsigned int GameManager::getResolutionHeight() const {
    return resolution.y;
}

// ********** Setters **********

void GameManager::setResolution(const sf::Vector2<unsigned int> new_resolution) {
    resolution = new_resolution;
    is_custom_resolution = true;
}

/* ===========================================================================================================================================================================================
 * ===   FULL SCREEN   =======================================================================================================================================================================
 * ===========================================================================================================================================================================================
*/

// ********** Getters **********

bool GameManager::isFullscreen() const {
    return fullscreen;
}

bool GameManager::isExclusiveFullscreen() const {
    return exclusive_fullscreen;
}

// ********** Setters **********

void GameManager::toggleFullscreen() {
    fullscreen = !fullscreen;
}

void GameManager::toggleExclusiveFullscreen() {
    exclusive_fullscreen = !exclusive_fullscreen;
}

/* ===========================================================================================================================================================================================
 * ===   VSYNC   =============================================================================================================================================================================
 * ===========================================================================================================================================================================================
*/

// ********** Getters **********

bool GameManager::isVSyncEnabled() const {
    return vsync;
}

// ********** Setters **********

void GameManager::toggleVsync() {
    vsync = !vsync;
}

/* ===========================================================================================================================================================================================
 * ===   SCALE   =============================================================================================================================================================================
 * ===========================================================================================================================================================================================
*/

// ********** Getters **********

float GameManager::getUIScale() const {
    return scale_ui;
}

// ********** Setters **********

void GameManager::setUIScale(const float scale) {
    // Limit the scale to a minimum of 10% and a maximum of 800%
    scale_ui = std::clamp(scale, 0.1f, 8.0f);
}